    ProcRemotos pr = new ProcRemotos();
    
    public void validar(String lic) {
        jLabel5.setText("Validando licencia...");
        if (pr.validarLic(lic)) {
            jLabel5.setText("Licencia correcta\nActivando EasyVentas");
            pr.activarLic(lic);
            jLabel5.setText("Felicidades ha activado EasyVentas con exito\nGracias por adquirir este software");
        } else {
            jLabel5.setText("Esta licencia no es valida o ya ha sido usada\nContacta a tu distibuidor");
        }
        jTextField1.setText("");
    }

