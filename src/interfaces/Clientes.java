        actualizarTabla(jTable4, lista);
        double saldo = 0.00;
        for (int i = 0; i < jTable4.getRowCount(); i++) {
            saldo += Double.parseDouble(jTable4.getValueAt(i, 5).toString());
        }
        BigDecimal big = new BigDecimal(saldo + "");
        big = big.setScale(2, RoundingMode.HALF_UP);
