    private void jTextField3KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            setVisible(false);
            puntoventa001.Main.interfaz.jTextField7.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String filtro = jTextField3.getText();
            actualizarTabla(jTable3, p.buscarClientes(filtro));
        } else {
            if (evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_ENTER) {
                jTable3.requestFocus();
                jTable3.setRowSelectionInterval(0, 0);
            } else {
                char ch = evt.getKeyText(evt.getKeyCode()).toLowerCase().charAt(0);
                if ((ch >= 97 && ch <= 122) || (ch >= 48 && ch <= 57)) {
                    String filtro = jTextField3.getText() + ch;
                    System.out.println(filtro);
                    actualizarTabla(jTable3, p.buscarClientes(filtro));
                }
            }

        }
    }//GEN-LAST:event_jTextField3KeyPressed

