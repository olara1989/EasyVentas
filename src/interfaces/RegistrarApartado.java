    public void enviar() {

        try {

            Cliente c = (Cliente) clientes.getSelectedItem();
            cliente = c.getId();
            abono = Double.parseDouble(anticipo.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            fechaLimite = sdf.format(fecha.getDate());
            echo = true;
            setVisible(false);

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Solo debes de colocar numeros y/o \nSeleccionar un Cliente", "ERROR!!!", 0);
        }
    }
