    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Clientes u = new Clientes(Main.interfaz, true);
        u.setVenta(true);
        u.jTabbedPane1.setSelectedIndex(1);
        u.nombre.requestFocus();
        u.setVisible(true);
        ArrayList<String[]> col = p.getAllClientes();

        clientes.removeAllItems();
        Cliente p = null;
        int num = 0;
        clientes.addItem(new Cliente(-1, "-- Selecciona --"));
        for (String[] strings : col) {
            p = new Cliente(Integer.parseInt(strings[0]), strings[1]);
            clientes.addItem(p);
            num++;
            if (p.getNombre().equals(u.name)) {
                clientes.setSelectedIndex(num);
            }

        }
        anticipo.requestFocus();

    }//GEN-LAST:event_jButton1ActionPerformed

