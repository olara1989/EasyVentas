    public void terminar() {
        p.cerrarTurno(Interfaz.usuario, InicioCaja.turno);
        Main.interfaz.login.entrada = false;
        Main.interfaz.login.inicio = false;
        Main.interfaz.login.show();
        user.setText("Sin usuario conectado");
        Main.interfaz.login.setAlwaysOnTop(true);
        Main.interfaz.jMenu2.setEnabled(false);
        Main.interfaz.jMenu4.setEnabled(false);
        Component[] comp = Main.interfaz.panelcapas.getComponents();
        for (int i = 0; i < comp.length; i++) {
            comp[i].setVisible(false);
        }
        Main.interfaz.login.jTextField1.requestFocus();
        Main.interfaz.login.jTextField1.setText("");
        Main.interfaz.login.jPasswordField1.setText("");
    }
