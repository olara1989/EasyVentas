public void comparar() {
        try {
            double hay = Double.parseDouble(jTextField1.getText());
            double diferencia = hay - esperado;
            jLabel8.setText("$" + diferencia + "0");
            if (diferencia == 0) {
                jLabel8.setForeground(Color.BLUE);
                jLabel9.setText("Excelente todo en orden");
            } else {
                jLabel8.setForeground(Color.RED);
                jLabel9.setText("Cantidad inesperada asi se registrará");
            }
        } catch (NumberFormatException numberFormatException) {
        }
    }
